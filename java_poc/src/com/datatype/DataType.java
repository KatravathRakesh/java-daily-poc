package com.datatype;

public class DataType {

	byte b ;
	short s;
	int i;
	long l;
	float f;
	double d;
	char c;
	boolean bo;
	
	
	public static void main(String[] args) {
		DataType s = new DataType();
		System.out.println("Byte b : "+s.b);
		System.out.println("short s : "+s.s);
		System.out.println("int i : "+s.i);
		System.out.println("long l : "+s.l);
		System.out.println("float f : "+s.f);
		System.out.println("double d : "+s.d);
		System.out.println("char c : "+s.c);
		System.out.println("boolean bo : "+s.bo); 
		
		
		
	}

}
